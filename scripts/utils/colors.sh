#!/bin/bash

# Define color codes
GREEN='\033[0;32m'
RED='\033[0;31m'
YELLOW='\033[1;33m'
BOLD='\033[1m'
NC='\033[0m' # No Color

# Function to print section header
print_section() {
  echo -e "\n"
  echo -e "${GREEN}=== $1 ===${NC}"
}

# Function to print error message
print_error() {
  echo -e "${RED}Error: $1${NC}"
}

# Function to print success message
print_success() {
  echo -e "${GREEN}$1${NC}"
}

# Function to print warning/info message
print_warning() {
  echo -e "${YELLOW}$1${NC}"
}

# Function to print bold text
print_bold() {
  echo -e "${BOLD}$1${NC}"
}

